package Canelas;

/**
 * Holds the x, y, and strength info of a gravity point*
 */
public class GravPoint {

    public double x, y, power;

    public GravPoint(double pX, double pY, double pPower) {
        x = pX;
        y = pY;
        power = pPower;
    }
    
    
}